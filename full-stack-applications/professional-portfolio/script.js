// Data flow animation
const dataFlow = document.querySelector('.data-flow');
if (dataFlow) {
    const nodes = dataFlow.querySelectorAll('.data-node');
    const arrows = dataFlow.querySelectorAll('.data-arrow');
    
    // Animate nodes sequentially
    nodes.forEach((node, index) => {
        setTimeout(() => {
            node.style.animation = 'pulse 1s ease-in-out';
        }, index * 1000);
    });
    
    // Animate arrows
    arrows.forEach((arrow, index) => {
        setTimeout(() => {
            arrow.style.animation = 'fadeInOut 2s ease-in-out infinite';
        }, index * 1000 + 500);
    });
}

